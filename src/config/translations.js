export const getTranslation = (key, language = DEFAULT_LANGUAGE, fallback) => {
  const candidates = [language, DEFAULT_LANGUAGE, "en"];

  for (const candidate of candidates) {
    const value = translations[candidate]?.[key];
    if (value) {
      return value;
    }
  }

  return fallback || key;
};

